/* A program to determine the class standing using a grade input by the user */

#include<stdio.h>

int main(){

  int grade;
  printf("\n Please enter an integer to indicate the range in which the grade falls: \n\n1 if >= 90%, \n2 if 70% <= grade < 90%,\n3 if 60% <= grade < 70%, \n4 if 50% <= grade < 60% and \n5 if grade < 50%) \n");
  scanf("%d", &grade);

  switch(grade){

  case 1:
	  printf("\n \033[0;31mGrade qualifies for Distinction \n");
	  break;

  case 2:
	  printf("\n Grade is First Class \n");
	  break;

  case 3:
	  printf("\n Grade is Second Class \n");
	  break;
  case 4:
	  printf("\n Grade is Third Class \n");
	  break;

  case 5:
	  printf("\n No credit granted with this grade. \n");
	  break;
  }
  printf("\n End of class standing assessment \n");

  return 0;
  
}
